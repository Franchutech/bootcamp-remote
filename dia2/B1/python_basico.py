# Tipos de datos en Python

# Entero (int)
numero_entero = 42
print(numero_entero, type(numero_entero))

# Decimal / punto flotante (float)
numero_decimal = 3.14
print(numero_decimal, type(numero_decimal))

# Cadena de texto (str)
texto = "Hola, soy FranchuTech"
print(texto, type(texto))

# Booleano (bool)
es_estudiante = True
print(es_estudiante, type(es_estudiante))
